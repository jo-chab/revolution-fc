        <h3>Pourquoi le DLTJ?</h3>
        <p>Nous ne nous attendons pas que les enfants participent à des concours d'orthographe sans d'abord leur apprendre leur alphabet. Lorsque les activités et les attentes ne correspondent pas avec l'étape de développement de l'enfant, ceci peut les mettre en place pour l'échec.</p>
        <p>La même chose est vraie lorsque de jeunes enfants sont plongés dans un environnement de soccer qui surestime “gagner” au détriment du développement des compétences et de son amusement. Les conséquences négatives peuvent durer toute une vie: certains enfants développent de mauvaises habitudes et de pauvres habiletés qui peuvent les décourager. Beaucoup n'atteignent pas leur potentiel de performance et d'autres finissent par quitter le sport parce qu'ils n'apprécient pas le jeu.</p>
        <p class="quote">Le DLTJ offre à tous les joueurs la meilleure chance de succès au soccer en adaptant la formation, la compétition et le coaching à leurs besoins à l'étape de développement approprié.</p>
        <p>Lorsque les enfants apprennent les bonnes compétences au bon moment et sont soutenus par une saine communauté de soccer composée de parents, d'entraîneurs, d'officiels et de bénévoles, ils deviennent plus confiants en leurs capacités et ont beaucoup plus de plaisir. Cela signifie que davantage d'entre eux deviendront des adultes sains et actifs avec un amour à vie pour le soccer.</p>
