        <h3>Qu'est-ce que le DLTJ?</h3>
        <p>DLTJ est un modèle de développement des athlètes parallèle à celui que les docteurs et psychologues utilisent depuis longtemps pour mieux comprendre le développement humain et qui consiste en une série d'étapes de l'enfance jusqu'à l'âge adulte. D'étape en étape, les habiletés d'une personne, son comportement, ses émotions ainsi que sa compréhension du monde qui l'entoure varient.</p>
        <p>De façon similaire, les experts en science du sport ont identifié 7 étapes de développement, chacune avec ses propres caractéristiques physiques et psychologiques qui forme la base du DLTJ. Lorsque les jeunes joueurs peuvent s'entraîner et compétitionner dans un environnement qui est approprié avec leurs étapes de développement, ils performent à leur meilleur et ils ont plus de plaisir.</p>
        <p class="quote">En d'autres termes, DLTJ est bâti pour donner aux joueurs une expérience de soccer optimale à chaque étape en mettant leurs besoins vers l'avant.</p>
